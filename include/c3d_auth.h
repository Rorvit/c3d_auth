/**
 * @file c3d_auth.h
 * @brief Проверка авторизации по публичному ключу.
 */

#pragma once

#include <string>

 /**
  * @brief Проверяет, соответствует ли переданный публичный ключ зашитому приватному.
  *
  * Функция загружает встроенный приватный ключ и переданный публичный ключ (в формате PEM),
  * извлекает параметры RSA (модуль n и экспоненту e) и сравнивает их.
  *
  * @param public_key_pem Публичный ключ в формате PEM (строка, начинающаяся с "-----BEGIN PUBLIC KEY-----").
  * @return true  – если ключи образуют пару;
  * @return false – если ключи не совпадают, либо любой из ключей некорректен.
  *
  * @note Приватный ключ встроен в бинарный файл на этапе компиляции.
  */
bool check_authorization(const std::string& public_key_pem);